import {
  WASI,
  WASIProcExit,
  File,
  OpenFile,
  PreopenDirectory,
  ConsoleStdout,
} from '@bjorn3/browser_wasi_shim'

const wasmPath = 'static/coreutils.wasm'
const outputPath = 'src/generated/uutilsMetadata.ts'
const ansiPattern = /\x1b\[[0-?]*[ -/]*[@-~]/g
const lsColors = [
  'di=01;34',
  'ln=01;36',
  'ex=01;32',
  '*.txt=00;33',
  '*.md=00;33',
  '*.json=00;36',
  '*.js=00;32',
  '*.mjs=00;32',
  '*.py=00;35',
  '*.wasm=00;36',
].join(':')
const modulePromise = WebAssembly.compile(await Bun.file(wasmPath).arrayBuffer())

async function runCoreutils(args) {
  const module = await modulePromise
  const decoder = new TextDecoder()
  let stdout = ''
  let stderr = ''

  const fds = [
    new OpenFile(new File(new Uint8Array(), { readonly: true })),
    new ConsoleStdout((chunk) => {
      stdout += decoder.decode(chunk, { stream: true })
    }),
    new ConsoleStdout((chunk) => {
      stderr += decoder.decode(chunk, { stream: true })
    }),
    new PreopenDirectory('/', new Map()),
  ]

  const wasi = new WASI(
    ['coreutils', ...args],
    [
      'PATH=/bin',
      'HOME=/home/magni',
      'PWD=/home/magni',
      'USER=magni',
      'TERM=xterm-256color',
      'LC_ALL=C.UTF-8',
      'COLORTERM=truecolor',
      `LS_COLORS=${lsColors}`,
    ],
    fds,
    { debug: false },
  )
  const instance = await WebAssembly.instantiate(module, {
    wasi_snapshot_preview1: wasi.wasiImport,
  })

  let status = 0
  try {
    status = wasi.start(instance)
  } catch (error) {
    if (error instanceof WASIProcExit) {
      status = error.code
    } else {
      stderr += `${error instanceof Error ? error.message : String(error)}\n`
      status = 1
    }
  }

  return { stdout, stderr, status }
}

function uniqSorted(values) {
  return [...new Set(values)].sort((left, right) => left.localeCompare(right))
}

function stripAnsi(text) {
  return text.replace(ansiPattern, '')
}

function valueMode(placeholder) {
  return /\b(FILE|DIR|DIRECTORY|PATH|RFILE|SOURCE|TARGET|DEST)\b/i.test(placeholder) ? 'path' : 'raw'
}

function parseOptionValueFlags(helpText) {
  const raw = []
  const path = []

  for (const line of helpText.split('\n')) {
    const declaration = line.match(/^\s{2,}(-\S.*?)(?:\s{2,}|\t|$)/)?.[1]
    if (!declaration) {
      continue
    }

    const parsed = []
    for (const rawToken of declaration.split(',')) {
      const token = rawToken.trim()
      const long = token.match(/^(--[A-Za-z0-9][A-Za-z0-9-]+)(?:\[?=?<?([A-Za-z][A-Za-z0-9_-]*)>?\]?|\s+<?([A-Za-z][A-Za-z0-9_-]*)>?)?$/)
      if (long) {
        parsed.push({ flag: long[1], placeholder: long[2] ?? long[3] ?? null })
        continue
      }
      const short = token.match(/^(-[A-Za-z0-9])(?:\s*=?\s*<?([A-Za-z][A-Za-z0-9_-]*)>?)?$/)
      if (short) {
        parsed.push({ flag: short[1], placeholder: short[2] ?? null })
      }
    }

    const inferredPlaceholder = parsed.find((option) => option.placeholder)?.placeholder ?? null
    if (!inferredPlaceholder) {
      continue
    }
    const mode = valueMode(inferredPlaceholder)
    for (const option of parsed) {
      ;(mode === 'path' ? path : raw).push(option.flag)
    }
  }

  return {
    raw: uniqSorted(raw),
    path: uniqSorted(path),
  }
}

function hasFilesystemOperands(helpText) {
  const usage = helpText.match(/Usage:[\s\S]*?(?:\n\n|Options:|$)/i)?.[0] ?? ''
  return /\b(FILE|FILE1|FILE2|DIRECTORY|DIR|PATH|RFILE|SOURCE|TARGET|DEST)\b/i.test(usage)
}

function hasImplicitCwd(helpText) {
  return /current directory by default|^List directory contents\./im.test(helpText)
}

const listed = await runCoreutils(['--list'])
if (listed.status !== 0) {
  throw new Error(`coreutils --list failed:\n${listed.stderr || listed.stdout}`)
}

const commands = uniqSorted(listed.stdout.split('\n').map((line) => line.trim()).filter(Boolean))
const filesystemCommands = []
const implicitCwdCommands = []
const rawOptionValueFlags = {}
const pathOptionValueFlags = {}

for (const command of commands) {
  const result = await runCoreutils([command, '--help'])
  const helpText = stripAnsi(`${result.stdout}\n${result.stderr}`)
  if (hasFilesystemOperands(helpText)) {
    filesystemCommands.push(command)
  }
  if (hasImplicitCwd(helpText)) {
    implicitCwdCommands.push(command)
  }

  const flags = parseOptionValueFlags(helpText)
  if (flags.raw.length > 0) {
    rawOptionValueFlags[command] = flags.raw
  }
  if (flags.path.length > 0) {
    pathOptionValueFlags[command] = flags.path
  }
}

const source = `// Generated by scripts/generate-uutils-metadata.mjs from static/coreutils.wasm.
// Do not edit by hand.

export const UUTILS_COMMANDS = ${JSON.stringify(commands, null, 2)} as const

export const UUTILS_FILESYSTEM_COMMANDS = ${JSON.stringify(uniqSorted(filesystemCommands), null, 2)} as const

export const UUTILS_IMPLICIT_CWD_COMMANDS = ${JSON.stringify(uniqSorted(implicitCwdCommands), null, 2)} as const

export const UUTILS_OPTION_VALUE_FLAGS = ${JSON.stringify(rawOptionValueFlags, null, 2)} as const

export const UUTILS_OPTION_PATH_VALUE_FLAGS = ${JSON.stringify(pathOptionValueFlags, null, 2)} as const
`

await Bun.$`mkdir -p src/generated`
await Bun.write(outputPath, source)
